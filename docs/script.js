/* ==========================================================================
   Finance Analytics — dashboard logic
   Loads docs/data/dashboard_data.json (generated by src/web_export.py)
   and renders every chart + the searchable/sortable transactions table.
   No backend required — everything runs client-side, so this works as a
   static GitHub Pages site.
   ========================================================================== */

