let DATA = null;
let currentCategoryType = 'expense';

// -- Formatting helpers -----------------------------------------------------

function formatNumber(value, decimals = 0) {
  return new Intl.NumberFormat('ru-RU', {
    minimumFractionDigits: decimals,
    maximumFractionDigits: decimals,
  }).format(value);
}

function formatMoney(value) {
  return formatNumber(value, 0);
}

function animateNumber(el, target, formatter, duration = 1100) {
  const start = performance.now();
  const from = 0;
  function tick(now) {
    const progress = Math.min((now - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 3);
    el.textContent = formatter(from + (target - from) * eased);
    if (progress < 1) requestAnimationFrame(tick);
  }
  requestAnimationFrame(tick);
}

// -- Data loading -------------------------------------------------------

async function loadData() {
  const response = await fetch('data/dashboard_data.json');
  if (!response.ok) throw new Error('Не удалось загрузить данные дашборда');
  return response.json();
}

// -- Hero + KPI strip -----------------------------------------------------

function renderHero(data) {
  document.getElementById('generated-meta').textContent =
    `Данные обновлены: ${data.generated_at}`;

  const stampEl = document.getElementById('stamp-net-profit');
  animateNumber(stampEl, data.overall.net_profit, formatMoney);
}

function renderKpiGrid(data) {
  const overall = data.overall;
  const categories = data.categories;
  const kpis = [
    { label: 'Общий доход', value: formatMoney(overall.total_income) + ' ₸' },
    { label: 'Общий расход', value: formatMoney(overall.total_expense) + ' ₸' },
    { label: 'Чистая прибыль', value: formatMoney(overall.net_profit) + ' ₸', cls: overall.net_profit >= 0 ? 'positive' : 'negative' },
    { label: 'Всего операций', value: formatNumber(overall.total_transactions) },
    { label: 'Средний расход', value: formatMoney(overall.average_expense) + ' ₸' },
    { label: 'Средний доход', value: formatMoney(overall.average_income) + ' ₸' },
    { label: 'Макс. расход', value: formatMoney(overall.max_expense) + ' ₸' },
    { label: 'Макс. доход', value: formatMoney(overall.max_income) + ' ₸' },
    { label: 'Затратная категория', value: categories.most_expensive_category },
    { label: 'Прибыльная категория', value: categories.most_profitable_category },
    { label: 'Кол-во расходов', value: formatNumber(overall.total_expense_count) },
    { label: 'Кол-во доходов', value: formatNumber(overall.total_income_count) },
  ];

  const grid = document.getElementById('kpi-grid');
  grid.innerHTML = kpis.map(k => `
    <div class="kpi-cell">
      <div class="kpi-label">${k.label}</div>
      <div class="kpi-value ${k.cls || ''}">${k.value}</div>
    </div>
  `).join('');
}

// -- Charts: trends -----------------------------------------------------

function renderMonthlyTrend(data) {
  const m = data.monthly;
  Plotly.newPlot('chart-monthly-trend', [
    {
      x: m.months, y: m.income, name: 'Доход', mode: 'lines+markers',
      line: { color: COLORS.sage, width: 2.5 }, marker: { size: 6 },
    },
    {
      x: m.months, y: m.expense, name: 'Расход', mode: 'lines+markers',
      line: { color: COLORS.brick, width: 2.5 }, marker: { size: 6 },
    },
  ], {
    ...PLOTLY_BASE_LAYOUT,
    margin: { l: 60, r: 20, t: 10, b: 50 },
    legend: { orientation: 'h', y: 1.12, font: BASE_FONT },
    yaxis: { ...PLOTLY_BASE_LAYOUT.yaxis, title: { text: 'Сумма, ₸', font: BASE_FONT } },
  }, PLOTLY_CONFIG);
}

function renderGrowthCharts(data) {
  const m = data.monthly;
  const colorsFor = (values) => values.map(v => v >= 0 ? COLORS.sage : COLORS.brick);

  Plotly.newPlot('chart-expense-growth', [{
    x: m.months, y: m.expense_growth, type: 'bar',
    marker: { color: colorsFor(m.expense_growth) },
  }], { ...PLOTLY_BASE_LAYOUT, yaxis: { ...PLOTLY_BASE_LAYOUT.yaxis, title: { text: '%', font: BASE_FONT } } }, PLOTLY_CONFIG);

  Plotly.newPlot('chart-income-growth', [{
    x: m.months, y: m.income_growth, type: 'bar',
    marker: { color: colorsFor(m.income_growth) },
  }], { ...PLOTLY_BASE_LAYOUT, yaxis: { ...PLOTLY_BASE_LAYOUT.yaxis, title: { text: '%', font: BASE_FONT } } }, PLOTLY_CONFIG);
}

function renderHeatmap(data) {
  const cm = data.category_month_matrix;
  Plotly.newPlot('chart-heatmap', [{
    z: cm.values, x: cm.months, y: cm.categories,
    type: 'heatmap', colorscale: [
      [0, COLORS.paper], [0.5, COLORS.brickSoft], [1, COLORS.brick],
    ],
    hovertemplate: '%{y} · %{x}<br>%{z:,.0f} ₸<extra></extra>',
    colorbar: { tickfont: MONO_FONT, outlinewidth: 0 },
  }], {
    ...PLOTLY_BASE_LAYOUT,
    margin: { l: 110, r: 20, t: 10, b: 60 },
  }, PLOTLY_CONFIG);
}

// -- Charts: categories -----------------------------------------------------

function renderCategoryCharts(data, type) {
  const categories = data.categories;
  const percentRecords = type === 'expense' ? categories.expense_percent : categories.income_percent;
  const topRecords = type === 'expense' ? categories.top_expense : categories.top_income;

  document.getElementById('pie-title').textContent =
    type === 'expense' ? 'Доля категорий расходов' : 'Доля категорий доходов';
  document.getElementById('bar-title').textContent =
    type === 'expense' ? 'Топ-10 категорий расходов' : 'Топ-10 категорий доходов';

  Plotly.newPlot('chart-category-pie', [{
    labels: percentRecords.map(r => r.category),
    values: percentRecords.map(r => r.percent),
    type: 'pie', hole: 0.45,
    marker: { colors: CATEGORY_PALETTE, line: { color: COLORS.white, width: 1.5 } },
    textfont: MONO_FONT,
    hovertemplate: '%{label}<br>%{percent}<extra></extra>',
  }], {
    ...PLOTLY_BASE_LAYOUT,
    margin: { l: 10, r: 10, t: 10, b: 10 },
    showlegend: true,
    legend: { font: { ...BASE_FONT, size: 11 } },
  }, PLOTLY_CONFIG);

  const barColor = type === 'expense' ? COLORS.brick : COLORS.sage;
  Plotly.newPlot('chart-category-bar', [{
    x: topRecords.map(r => r.category),
    y: topRecords.map(r => r.amount),
    type: 'bar',
    marker: { color: barColor },
    hovertemplate: '%{x}<br>%{y:,.0f} ₸<extra></extra>',
  }], {
    ...PLOTLY_BASE_LAYOUT,
    xaxis: { ...PLOTLY_BASE_LAYOUT.xaxis, tickangle: -35 },
  }, PLOTLY_CONFIG);
}

function renderDistributionCharts(data) {
  const transactions = data.transactions;
  const amounts = transactions.map(t => t.amount);
  const expenseAmounts = transactions.filter(t => t.type === 'Expense').map(t => t.amount);

  Plotly.newPlot('chart-histogram', [{
    x: amounts, type: 'histogram', nbinsx: 30,
    marker: { color: COLORS.brass, line: { color: COLORS.white, width: 1 } },
  }], { ...PLOTLY_BASE_LAYOUT }, PLOTLY_CONFIG);

  Plotly.newPlot('chart-boxplot', [{
    y: expenseAmounts, type: 'box', name: 'Расходы',
    marker: { color: COLORS.brick }, boxpoints: 'outliers',
  }], { ...PLOTLY_BASE_LAYOUT, showlegend: false }, PLOTLY_CONFIG);

  const byDate = {};
  transactions.forEach(t => {
    if (!t.date) return;
    byDate[t.date] = byDate[t.date] || { income: 0, expense: 0 };
    if (t.type === 'Income') byDate[t.date].income += t.amount;
    else byDate[t.date].expense += t.amount;
  });
  const dailyIncome = Object.values(byDate).map(d => d.income);
  const dailyExpense = Object.values(byDate).map(d => d.expense);

  Plotly.newPlot('chart-scatter', [{
    x: dailyIncome, y: dailyExpense, mode: 'markers', type: 'scatter',
    marker: { color: COLORS.inkSoft, size: 8, opacity: 0.65, line: { color: COLORS.brass, width: 0.5 } },
    hovertemplate: 'Доход: %{x:,.0f} ₸<br>Расход: %{y:,.0f} ₸<extra></extra>',
  }], {
    ...PLOTLY_BASE_LAYOUT,
    xaxis: { ...PLOTLY_BASE_LAYOUT.xaxis, title: { text: 'Доход за день, ₸', font: BASE_FONT } },
    yaxis: { ...PLOTLY_BASE_LAYOUT.yaxis, title: { text: 'Расход за день, ₸', font: BASE_FONT } },
  }, PLOTLY_CONFIG);
}

